static struct ecc_curve curve_512a;

static bool curves_initialized = false;

static void gost_curves_init_internal(void)
{
	if (curves_initialized)
		return;

	/* --- 256 A --- */
	gost_vli_from_be(vli_p256a_p, p256a_p_be, 4);
	gost_vli_from_be(vli_p256a_a, p256a_a_be, 4);
	gost_vli_from_be(vli_p256a_b, p256a_b_be, 4);
	gost_vli_from_be(vli_p256a_n, p256a_n_be, 4);
	gost_vli_from_be(vli_p256a_gx, p256a_gx_be, 4);
	gost_vli_from_be(vli_p256a_gy, p256a_gy_be, 4);

	curve_256a.name		 = "gost-ec256-paramSetA";
	curve_256a.nbits	 = 256;
	curve_256a.p		 = vli_p256a_p;
	curve_256a.a		 = vli_p256a_a;
	curve_256a.b		 = vli_p256a_b;
	curve_256a.n		 = vli_p256a_n;
	curve_256a.g.x		 = vli_p256a_gx;
	curve_256a.g.y		 = vli_p256a_gy;
	curve_256a.g.ndigits = 4;

	/* --- 512 A --- */
	gost_vli_from_be(vli_p512a_p, p512a_p_be, 8);
	gost_vli_from_be(vli_p512a_a, p512a_a_be, 8);
	gost_vli_from_be(vli_p512a_b, p512a_b_be, 8);
	gost_vli_from_be(vli_p512a_n, p512a_n_be, 8);
	gost_vli_from_be(vli_p512a_gx, p512a_gx_be, 8);
	gost_vli_from_be(vli_p512a_gy, p512a_gy_be, 8);

	curve_512a.name		 = "gost-ec512-paramSetA";
	curve_512a.nbits	 = 512;
	curve_512a.p		 = vli_p512a_p;
	curve_512a.a		 = vli_p512a_a;
	curve_512a.b		 = vli_p512a_b;
	curve_512a.n		 = vli_p512a_n;
	curve_512a.g.x		 = vli_p512a_gx;
	curve_512a.g.y		 = vli_p512a_gy;
	curve_512a.g.ndigits = 8;

	curves_initialized = true;
}

const struct ecc_curve *gost_get_curve(unsigned int curve_id)
{
	if (!curves_initialized)
		gost_curves_init_internal();

	if (curve_id == GOST_CURVE_256A)
		return &curve_256a;
	if (curve_id == GOST_CURVE_512A)
		return &curve_512a;
	return NULL;
}
